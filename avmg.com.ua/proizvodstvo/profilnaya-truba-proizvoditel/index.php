<h3>Производство труб</h3>
<p>Производство стальных труб может вестись несколькими способами.</p>
В зависимости от выбора сырья и метода изготовления различают:
<ul>
	<li>Электросварные прямошовные трубы</li>
	<li>Электросварные спиральношовные</li>
	<li>Бесшовные горячедеформированные</li>
	<li>Бесшовные холоднокатаные</li>
</ul><br>

<p>Основные этапы производства:</p>
Электросварные прямошовные
<ul>
	<li>Штрипс — свернутый в рулон стальной лист — разматывается и режется на продольные полосы</li>
	<li>Полосы свариваются в бесконечную ленту, что необходимо для обеспечения непрерывности производства</li>
	<li>Лента в вальцах деформируется, превращаясь в круглую в сечении заготовку с открытым швом</li>
	<li>Шов проваривается. Применяется несколько типов сварки — дуговая, индукционными токами, лазером, плазмой и пучками электронов</li>
</ul><br>

<p>Ниболее широко применяются два метода сварки шва — TIG (электродуговая сварка вольфрамовым электродом в среде инертного газа) и HF (сварка индукционными токами высокой частоты).</p>
<p>Шов, выполненный первой, несколько прочнее; зато второй метод сварки примерно в 20 раз быстрее, соответственно сваренные HF трубы заметно дешевле.</p>
<ul>
	<li>Затем стальная круглая труба калибруется в вальцах</li>
	<li>Проходит неразрушающий контроль шва вихревыми токами или ультразвуком</li>
	<li>Режется на отрезки необходимой длины и складируется.</li>
</ul><br>

<h3>Электросварные спиральношовные</h3>
<p>Само производство этих труб выглядит примерно так же, как у прямошовных; однако лента заготовки сворачивается вальцами не в трубку, а в спираль. Спиральный шов обеспечивает трубе большую прочность на разрыв.</p>

<h3>Бесшовные горячедеформированные</h3>
Бесшовная (или, как ее еще называют, цельнотянутая) труба методом горячей деформации производится из цилиндрической монолитной заготовки:
<ul>
	<li>Заготовка разогревается в печи</li>
	<li>Прошивной пресс превращает ее в гильзу — полый цилиндр</li>
	<li>Горячая гильза проходит несколько вальцов, вытягиваясь (отсюда термин «цельнотянутые трубы») и приобретая нужные диаметр и толщину стенок</li>
	<li>Затем готовая круглая стальная труба остужается, режется по длине и отправляется на склад готовой продукции.</li>
</ul><br>

<h3>Бесшовные холоднодеформированные</h3>
Отличия от предыдущих труб всего два:
<ul>
	<li>После прошивного стана гильза охлаждается и все последующие операции проходит в холодном виде</li>
	<li>По окончании формирования трубы она обязательно отжигается — нагревается до температуры рекристаллизации стали и охлаждается</li>
</ul><br>

<p>Это делает сталь более вязкой; кроме того, металл покидают неизбежные при холодной деформации внутренние напряжения.</p>
<p>Рынок предлагает бесшовные холоднодеформированные (холоднокатаные) трубы с толщиной стенок от 0,3 до 24 мм при диаметре 5 — 250 мм.</p>

